"use client";

import { useState } from "react";

export default function AIUpload() {
  const [file, setFile] = useState<File | null>(null);
  const [report, setReport] = useState<any>(null);
  const [loading, setLoading] = useState(false);

  const handleUpload = async () => {
    if (!file) return alert("Select file first");

    if (!file.name.endsWith(".csv")) {
      return alert("Please upload a CSV file only");
    }

    setLoading(true);
    setReport(null);

    try {
      const formData = new FormData();
      formData.append("file", file);

      const res = await fetch("/api/ai-report", {
        method: "POST",
        body: formData,
      });

      if (!res.ok) throw new Error("API failed");

      const data = await res.json();
      setReport(data);
    } catch (err) {
      console.error(err);
      alert("Something went wrong ❌");
    } finally {
      setLoading(false);
    }
  };

  // ✅ Generate Report Text (for download & copy)
  const generateReportText = () => {
    if (!report) return "";

    return `========================
        AI REPORT
========================

${report.summary}

------------------------
Anomaly:
${report.anomaly}

------------------------
Suggestion:
${report.suggestion}

========================
Generated by AI System`;
  };

  // ✅ Download handler (fixed memory leak)
  const handleDownload = () => {
    const blob = new Blob([generateReportText()], {
      type: "text/plain",
    });

    const url = URL.createObjectURL(blob);

    const a = document.createElement("a");
    a.href = url;
    a.download = "AI_Report.txt";
    a.click();

    URL.revokeObjectURL(url); // 🔥 important fix
  };

  // ✅ Copy handler
  const handleCopy = () => {
    navigator.clipboard.writeText(generateReportText());
    alert("Copied to clipboard ✅");
  };

  // ✅ Clear handler
  const handleClear = () => {
    setReport(null);
    setFile(null);
  };

  return (
    <div className="bg-white border p-6 mt-6 rounded-lg shadow-sm">
      <h2 className="text-xl font-bold mb-4">AI Data Analysis</h2>

      {/* FILE INPUT */}
      <input
        type="file"
        accept=".csv"
        onChange={(e) => setFile(e.target.files?.[0] || null)}
        className="mb-4"
      />

      {/* FILE NAME */}
      {file && (
        <p className="text-sm text-gray-600 mb-3">
          Selected: <b>{file.name}</b>
        </p>
      )}

      {/* BUTTON */}
      <button
        onClick={handleUpload}
        disabled={loading}
        className={`px-4 py-2 rounded text-white ${
          loading
            ? "bg-gray-400 cursor-not-allowed"
            : "bg-orange-400 hover:bg-orange-500"
        }`}
      >
        {loading ? "Processing..." : "Generate AI Report"}
      </button>

      {/* REPORT */}
      {report && (
        <>
          <div className="mt-4 p-5 bg-blue-50 rounded border text-sm leading-relaxed">

            <h3 className="text-lg font-bold mb-2">📊 AI REPORT</h3>

            <p className="whitespace-pre-line">
              <b>📌 Summary:</b>{"\n"}
              {report.summary}
            </p>

            <p className="mt-3">
              <b>⚠️ Anomaly:</b> {report.anomaly}
            </p>

            <p>
              <b>💡 Suggestion:</b> {report.suggestion}
            </p>

          </div>

          {/* ACTION BUTTONS */}
          <div className="flex gap-3 mt-3 flex-wrap">

            <button
              onClick={handleDownload}
              className="bg-green-600 text-white px-4 py-2 rounded hover:bg-green-700"
            >
              📥 Download Report
            </button>

            <button
              onClick={handleCopy}
              className="bg-blue-600 text-white px-4 py-2 rounded hover:bg-blue-700"
            >
              📋 Copy Report
            </button>

            <button
              onClick={handleClear}
              className="bg-red-500 text-white px-4 py-2 rounded hover:bg-red-600"
            >
              ❌ Clear
            </button>

          </div>
        </>
      )}
    </div>
  );
}